function reverseArrayInPlace(array) {
  let newArray = []
 for(let i = array.length -1; i >= 0; i--){
 newArray.push(array[i])
 }
 for(let y = 0; y < array.length; y++){
   array[y] = newArray[y]
 }
 return array;
 }

