// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(numb) {
  //if num equals zero its positive
if(numb === 0) return true;
//if num has a remainder of 1 its negative
else if(numb === 1) return false;
//if its negative add 2 instead of subtract until 1 or 0
else if(numb < 0) return isEven(numb + 2);
//keep subracting two from the numb until its 1 or 0
else return isEven(numb - 2);
}

