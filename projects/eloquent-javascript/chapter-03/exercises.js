function countChars(string, char){
  //make a variable to kep the count
  let count = 0
  //loop through the string
  for(let i = 0; i < string.length; i++)
  //if the string === char
  if(string[i] === char)
  //add 1 to the count
  count += 1
  //return the count
  return count
  }

